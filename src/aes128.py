def expand_key(key):
    w = 44 * [[]]
    w[0:4] = [key[0:4], key[4:8], key[8:12], key[12:16]]
    for i in range(4, 44):
        if i % 4 == 0:
            tmp = sub_word(rot_word(w[i-1]))
            tmp[0] = tmp[0] ^ RCON[int(i/4)]
        else:
            tmp = w[i-1]
        w[i] = [tmp[0] ^ w[i-4][0], tmp[1] ^ w[i-4][1], tmp[2] ^ w[i-4][2], tmp[3] ^ w[i-4][3]]
    return w


def rot_word(w):
    ret = len(w) * [None]
    for i in range(len(w)):
        ret[i] = w[(i+1) % len(w)]
    return ret


def sub_word(w):
    for i in range(len(w)):
        w[i] = SBOX[w[i]]
    return w


def sub_state(state):
    for i in range(len(state)):
        for j in range(len(state[i])):
            state[i][j] = SBOX[state[i][j]]
    return state


def shift_rows(state):
    s_0 = state[0]
    s_1 = [state[1][1], state[1][2], state[1][3], state[1][0]]
    s_2 = [state[2][2], state[2][3], state[2][0], state[2][1]]
    s_3 = [state[3][3], state[3][0], state[3][1], state[3][2]]
    return [s_0, s_1, s_2, s_3]


def mix_columns(state):
    cpy = copy.deepcopy(state)
    for i in range(0, 4):
        state[0][i] = gal_mul(cpy[0][i], 2) ^ cpy[3][i] ^ cpy[2][i] ^ gal_mul(cpy[1][i], 3)
        state[1][i] = gal_mul(cpy[1][i], 2) ^ cpy[0][i] ^ cpy[3][i] ^ gal_mul(cpy[2][i], 3)
        state[2][i] = gal_mul(cpy[2][i], 2) ^ cpy[1][i] ^ cpy[0][i] ^ gal_mul(cpy[3][i], 3)
        state[3][i] = gal_mul(cpy[3][i], 2) ^ cpy[2][i] ^ cpy[1][i] ^ gal_mul(cpy[0][i], 3)
    return state


def add_round_key(state, round_key):
    for i in range(0, 4):
        for j in range(0, 4):
            state[i][j] = state[i][j] ^ round_key[j][i]
    return state


def gal_mul(x, y):
    #if (x == 0) or (y == 0):
    #    return 0
    x_log = LOG[x]
    y_log = LOG[y]
    exp_value = (x_log + y_log) % 255
    return EXP[exp_value]


